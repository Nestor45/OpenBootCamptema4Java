public class SmartDevice {

    // Atributos
    String color;
    String marca;
    String modelo;
    String anio;
    String capacidad;
    String tipoPantalla;
    // Construcctor
    public SmartDevice() {

    }

    public SmartDevice(String color, String marca, String modelo, String anio, String capacidad, String tipoPantalla) {
        this.color = color;
        this.marca = marca;
        this.modelo = modelo;
        this.anio = anio;
        this.capacidad = capacidad;
        this.tipoPantalla = tipoPantalla;
    }
    

}
